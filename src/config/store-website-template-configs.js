export function getTemplateConfig(templateId) {
  return TEMPLATE_CONFIGS[templateId] || TEMPLATE_CONFIGS['product-01'];
}

export function resolveStoreTheme(templateId, contentConfig) {
  const config = getTemplateConfig(templateId);
  const customPromo = contentConfig?.promo?.trim() || '';
  return {
    palette: config.palette,
    config,
    isSerif: config.font === 'serif',
    promo: customPromo,
    features: config.features || [],
    family: config.family,
  };
}
